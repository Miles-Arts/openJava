package company.company;

public class ForLoop {

    public static void main(String[] args) {

        // los for sirven con Arrays
        //   comienzo | Parada | Paso
         for (int i = 0; i <= 10; i++ ) { //Estructura de control determinada
             System.out.println("Print: " + i + " Hi Startups");
         }
        System.out.println("for terminado");
    }

}
